#include<stdio.h>

int main(){

     // int  %d;

    int num = 31;

    printf("%d", num);

    // float  %f;

    float decimalValue = 31.0;

    printf("\n%f", decimalValue);

    // char  %c

    char character = 'n';

    printf("\n%c", character);

    // string  %s

    char string[10] = "hii";

    printf("\n%s", string);

    return 0;
}